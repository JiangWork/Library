package com.klatencor.klara.future.object.recipe;

public class LightMode {

	private String illum;
	private String grayscale;
	private String quartzPhaseDegrees;
	private String useForGain;
	public String getIllum() {
		return illum;
	}
	public void setIllum(String illum) {
		this.illum = illum;
	}
	public String getGrayscale() {
		return grayscale;
	}
	public void setGrayscale(String grayscale) {
		this.grayscale = grayscale;
	}
	public String getQuartzPhaseDegrees() {
		return quartzPhaseDegrees;
	}
	public void setQuartzPhaseDegrees(String quartzPhaseDegrees) {
		this.quartzPhaseDegrees = quartzPhaseDegrees;
	}
	public String getUseForGain() {
		return useForGain;
	}
	public void setUseForGain(String useForGain) {
		this.useForGain = useForGain;
	}
}
